window.onscroll = function() {
  var currentScrollPos = window.pageYOffset;
  if (currentScrollPos > 600) {
    document.getElementById("navigation").style.top = "-50px";
  } else {
    document.getElementById("navigation").style.top = "0px";

  }
}


